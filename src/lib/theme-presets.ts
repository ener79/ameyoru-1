const DANGEROUS_PATTERNS = [
  /url\s*\(/i,
  /expression\s*\(/i,
  /@import/i,
  /<script/i,
  /javascript:/i,
  /behavior\s*:/i,
  /-moz-binding/i,
];

export function validateCustomCSS(css: string): string | null {
  if (css.length > 10240) return "自定义 CSS 不能超过 10KB";
  for (const p of DANGEROUS_PATTERNS) {
    if (p.test(css)) return "CSS 中包含不安全的内容";
  }
  return null;
}
