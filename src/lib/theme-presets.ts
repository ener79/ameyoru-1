function t(light: Record<string, string>, dark: Record<string, string>): string {
  const toBlock = (vars: Record<string, string>) =>
    Object.entries(vars).map(([k, v]) => `--${k}:${v}`).join(";");
  return `:root{${toBlock(light)}}.dark{${toBlock(dark)}}`;
}

